package com.club.entidades;

public class Adicional {
	
	private int id_adicional;
	private double costo_adicional;
	private String unidad;
	
	public int getId_adicional() {
		return id_adicional;
	}
	public void setId_adicional(int id_adicional) {
		this.id_adicional = id_adicional;
	}
	public double getCosto_adicional() {
		return costo_adicional;
	}
	public void setCosto_adicional(double costo_adicional) {
		this.costo_adicional = costo_adicional;
	}
	public String getUnidad() {
		return unidad;
	}
	public void setUnidad(String unidad) {
		this.unidad = unidad;
	}
	
	

}
